package edu.study.vo;

public class BoardVO {

	private int uno;
	private int bno;
	private String btitle;
	private String bkind;
	private String bnote;
	private String bwdate;
	private String bhit;

	
	
	
	
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public String getBhit() {
		return bhit;
	}
	public void setBhit(String bhit) {
		this.bhit = bhit;
	}
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getBtitle() {
		return btitle;
	}
	public void setBtitle(String btitle) {
		this.btitle = btitle;
	}
	public String getBkind() {
		return bkind;
	}
	public void setBkind(String bkind) {
		this.bkind = bkind;
	}
	public String getBnote() {
		return bnote;
	}
	public void setBnote(String bnote) {
		this.bnote = bnote;
	}
	public String getBwdate() {
		return bwdate;
	}
	public void setBwdate(String bwdate) {
		this.bwdate = bwdate;
	}
	
	
}
