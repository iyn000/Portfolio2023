package my.spring.vo;

public class BoardVO {

	private int uno;
	private int bno;
	private String btitle;
	private String bnote;
	private String bwdate;
	private String bhit;
	private String uname;
 


	// getters and setters
	
	public String getUname() {
		return uname;
	}
	public void setUname(String uname) {
		this.uname = uname;
	}
	public int getUno() {
		return uno;
	}
	public void setUno(int uno) {
		this.uno = uno;
	}
	public int getBno() {
		return bno;
	}
	public void setBno(int bno) {
		this.bno = bno;
	}
	public String getBtitle() {
		return btitle;
	}
	public void setBtitle(String btitle) {
		this.btitle = btitle;
	}
	public String getBnote() {
		return bnote;
	}
	public void setBnote(String bnote) {
		this.bnote = bnote;
	}
	public String getBwdate() {
		return bwdate;
	}
	public void setBwdate(String bwdate) {
		this.bwdate = bwdate;
	}
	public String getBhit() {
		return bhit;
	}
	public void setBhit(String bhit) {
		this.bhit = bhit;
	}

	
	
	
}
